<?php

// проверка на белорусский номер телефона, начинается с +375 , доступные кода 29,25,44,33 , потом идет 7 цифр первая из которых не 0

$numbers = [
    '+3752976394205',
    '+375267639420',
    '+374297639420',
    '375297639420',
    '+375290639420',
    '+375297639420',
];

foreach ($numbers as $number) {
    $result = preg_match('/^\+375(33|29|44|25)[1-9][0-9]{6}$/', $number);
    print_r('Result - ' . $result . PHP_EOL);
}

